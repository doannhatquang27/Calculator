    /**
     * Convert the number which is displayed on the screen to Double
     */
    private double ConvertDisplayToNumber(String displayString) {
        return Double.parseDouble(displayString);
    }
    
    private void calculatePre() {
        if (!equalButtonIsClicked && !expressionIsClicked) {
            recentValue = ConvertDisplayToNumber(txtDisplay.getText());
        }
        
        if (equalButtonIsClicked) {
            result = recentValue = ConvertDisplayToNumber(txtDisplay.getText());
            equalButtonIsClicked = false;
        }
        
        switch (currentOperation) {
            case '+': {
                result += recentValue;
                break;
            }
            case '-': {
                result -= recentValue;
                break;
            }
            case '*': {
                result *= recentValue;
                break;
            }
            case '/': {
                if (recentValue != 0.0) {
                    result /= recentValue;
                } else {
                    txtDisplay.setText("Error");
                }
                break;
            }
            case '#': {
                result = ConvertDisplayToNumber(txtDisplay.getText());
                break;
            }
            case 'd': break;
        }
    }
        
    
    private void displayResult() {
        String resultString = result.toString();
        if (resultString.contains(".0")) {
            resultString = resultString.substring(0, resultString.length() - 2);
        }
        txtDisplay.setText(resultString);
    }
    
    private void displayTempResult(Double tmpResult) {
        String resultString = tmpResult.toString();
        if (resultString.contains(".0")) {
            resultString = resultString.substring(0, resultString.length() - 2);
        }
        txtDisplay.setText(resultString);
    }
    
